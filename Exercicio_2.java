public class Exercicio_2 {
    /*EXERCICIO 2: Faça as 4 operações basicas: adição, subtração, multiplicação e divisão.*/
    
    public static void main(String[] args) {
        int Numero_1 = 5;
        int Numero_2 = 5;

        System.out.println("Adição = " + (Numero_1 + Numero_2));
        System.out.println("Subtração = " + (Numero_1 - Numero_2));
        System.out.println("Multiplicação = " + (Numero_1 * Numero_2));
        System.out.println("Divisão = " + (Numero_1 / Numero_2));
        
    }
    
}
